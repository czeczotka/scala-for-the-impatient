package com.czeczotka.scala.impatient.chapter06

object Conversions {
  
  def inchesToCentimeters(inches: Double): Double = {
    inches * 2.54
  }
  
  def gallonsToLiters(gallons: Double): Double = {
    gallons * 4.54609
  }
  
  def milesToKilometers(miles: Double): Double = {
    miles * 1.609344
  }

}